using System;

namespace Sample1
{
    public class Class1
    {
        public int Sum(int a, int b)
        {
            return a + b;
        }
        public int Sub(int a, int b)
        {
            return a - b;
        }
        public int Div(int a, int b)
        {
            return a / b;
        }
        public int Product(int a, int b)
        {
            return a * b;
        }
    }
}
